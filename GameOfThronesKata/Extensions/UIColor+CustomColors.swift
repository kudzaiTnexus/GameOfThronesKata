import Foundation

import Foundation
import UIKit

extension UIColor
{
    class func discoveryThemeGrey() -> UIColor
    {
        return UIColor(red: 139.0/255.0, green: 139.0/255.0, blue: 139.0/255.0, alpha:1.0)
    }
    
    class func discoveryThemeBlack() -> UIColor
    {
        return UIColor(red: 31.0/255.0, green: 31.0/255.0, blue: 31.0/255.0, alpha:1.0)
    }
    
    class func discoveryOffWhite() -> UIColor
    {
        return UIColor(red: 250.0/255.0, green: 250.0/255.0, blue: 250.0/255.0, alpha:1.0)
    }
    
}
